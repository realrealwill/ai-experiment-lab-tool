import random

def shuffle(df, random_state=None):
    shuffled_df = df.sample(frac=1, random_state=random_state).reset_index(drop=True)

    # pair_id  String version of term_pair_id, formatted as "pair_001", "pair_002", etc. for the prompt use.
    shuffled_df["pair_id"] = shuffled_df["term_pair_id"].apply(
        lambda x: f"pair_{x:03d}"
    )

    shuffled_df["prompt_position"] = range(1, len(shuffled_df) + 1)

    return shuffled_df

def totext(shuffled_df):
    lines = []
    for _, row in shuffled_df.iterrows():
        lines.append(f'{row["pair_id"]}: {row["term_pair"]}')

    return "\n".join(lines)


def build_prompt(shuffled_df, run_uuid):
    text = totext(shuffled_df)

    prompt = f"""
    You are an expert computational linguist and AI evaluator. Your task is to evaluate a list of word-pairs generated by unsupervised topic models and determine if each pair represents a meaningful, coherent topic or concept in statistical research. Note that terms can be stemmed, and you should take this into consideration.

    EVALUATION CRITERIA:
    - Score 1 (Meaningful/Correct): The two words are semantically related, frequently co-occur in a specific context, or form a clearly recognizable concept in statistical research.
    - Score 0 (Nonsense/Incorrect): The two words are unrelated, randomly paired, or rely on uninformative generic words without forming a distinct topic, or fail to give a particular statistical meaning though they can appear together.

    OUTPUT FORMAT:
    You must output EXACTLY 71 items without duplication. 
    You must output ONLY a strictly valid JSON array of objects.
    Do not include any markdown formatting, explanations, or conversational text. 
    Do not add trailing commas.
    CRITICAL INSTRUCTION: You must evaluate and output the pairs in the EXACT SAME ORDER they appear in the input list below, do not miss any pairs. Do not sort them alphabetically. No ```json or any other formatting, just the raw JSON array.


    EXAMPLE EXPECTED OUTPUT:
    [
        {{"id": "pair_042", "label": 1}},
        {{"id": "pair_007", "label": 0}},
        {{"id": "pair_103", "label": 1}}
    ]


    [System Run ID: {run_uuid}]

    HERE ARE THE WORD PAIRS TO EVALUATE:
    {text}
    """.strip()

    return prompt